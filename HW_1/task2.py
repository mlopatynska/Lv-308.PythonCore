from math import sin, exp, log

a = float(input("What's value of a?\n"))
b = float(input("What's value of b?\n"))
print("a + b = {}".format(a + b))
print("a * b= {}".format(a * b))
print("a - b = {}".format(a - b))
print("a / b = {}".format(a / b))
print("sin (a) = {}".format(sin(a)))
print("exp(b) = {}".format(exp(b)))
print("log a(b) = {}".format(log(b, a)))

